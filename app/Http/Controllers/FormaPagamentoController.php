     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $fpg = FormaPagamento::find($id);
        if($request->has('descricao')){
            $fpg->descricao = $request->get('descricao');
        }
        $fpg->save();
        return $fpg;
    }

